// Pengulangan While:

var nilaiAwal = 1;
while( nilaiAwal <= 10 ) {
    console.log('Hello World! ' + nilaiAwal + "x");
    nilaiAwal++;
}

// Contoh: Buat pengulangan console.log('Cappie Kawaii :3') sebanyak 10 kali menggunakan for
for ( var helloWorld = 1; helloWorld <= 10; helloWorld++ ) {
    console.log('Cappie Kawaii :3 ' + helloWorld + "x")
}

